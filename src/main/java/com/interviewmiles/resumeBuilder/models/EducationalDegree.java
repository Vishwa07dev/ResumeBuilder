package com.interviewmiles.resumeBuilder.models;

public class EducationalDegree {
	
	private String instituteName ;
	private String percent_grade ;
	private String status ;
	private String degree ;
	private String location ;
	private String completionYear ;
	private String description ;
	public String getInstituteName() {
		return instituteName;
	}
	public void setInstituteName(String instituteName) {
		this.instituteName = instituteName;
	}
	public String getPercent_grade() {
		return percent_grade;
	}
	public void setPercent_grade(String percent_grade) {
		this.percent_grade = percent_grade;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getDegree() {
		return degree;
	}
	public void setDegree(String degree) {
		this.degree = degree;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getCompletionYear() {
		return completionYear;
	}
	public void setCompletionYear(String completionYear) {
		this.completionYear = completionYear;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	

}
